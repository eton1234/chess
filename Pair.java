
public class Pair
{
    // instance variables - replace the example below with your own
    int x;
    int y;
    public Pair(int x,int y) 
    {
        this.x = x;
        this.y = y;
    }
    public int getX (){
        return this.x;
    }
    public int getY (){
        return this.y;
    }
    public void setX (int x) {
        this.x = x;
    }
    public void setY (int y) {
        this.y = y;
    }
}
